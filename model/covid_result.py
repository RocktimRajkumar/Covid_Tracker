class CovidResult:

    def set_user_id(self, user_id):
        self.user_id = user_id

    def get_user_id(self):
        return self.user_id

    def set_created_by(self, admin_id):
        self.created_by = admin_id

    def get_created_by(self):
        return self.created_by

    def set_result(self, result):
        self.result = result

    def get_result(self):
        return self.result
