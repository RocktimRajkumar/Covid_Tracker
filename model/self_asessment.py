class SelfAsessment:

    def set_user_id(self, user_id):
        self.user_id = user_id

    def get_user_id(self):
        return self.user_id

    def set_symptoms(self, symptoms):
        self.symptoms = symptoms

    def get_symptoms(self):
        return self.symptoms

    def set_travel_history(self, travel_history):
        self.travel_history = travel_history

    def get_travel_history(self):
        return self.travel_history

    def set_contact_with_covid_patient(self, contact_with_covid_patient):
        self.contact_with_covid_patient = contact_with_covid_patient

    def get_contact_with_covid_patient(self):
        return self.contact_with_covid_patient
